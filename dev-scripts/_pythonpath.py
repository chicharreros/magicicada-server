"""Add the main lib directory to the Python path."""

import os
import sys


def get_lib_dir():
    """Build the main lib directory path"""
    return os.path.join(os.path.dirname(__file__), os.pardir, 'lib')

sys.path.insert(0, get_lib_dir())
