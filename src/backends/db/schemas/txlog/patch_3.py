"""Adding an index for timestamp in the txlog tables."""

SQL = """
    CREATE INDEX transaction_log__timestamp__date__idx ON
    txlog_transaction_log(CAST("timestamp" AS date));
    """


def apply(store):
    """Apply the patch"""
    store.execute(SQL)
